    /**
     * 扫描并记录
     * 关于逻辑的思考
     * 1. 首先，确认三个值 min mid max
     * 2. 然后 min 会被首先初始化
     * 3. 遇到小于 min的值 会继续赋值 min 不会进入到 mid
     * 4. 如果初始化完 min ，第一次遇到了大于min的值，则初始化给 mid
     * 5. 此时 min 和 mid 都已经被初始化过了，只要有值初始化给，max就算是题目条件成立了
     * 6. 但是：思考？
     * 7. 初始化完 min和mid后，后续值全都小于min，则继续初始化min，不会赋值max
     * 8. 初始化完 min 和 mid后，后续值的情况如果是 有小于min的值和大于mid的值，则遇到小于min的就赋值给min，但是错误值依然通不过mid
     * 9. 可以说 min和mid初始化完以后就是一个数据筛选的过滤器只要通过了min和mid则是正确的数据
     *
     * @param nums 待处理的数据
     * @return 是否存在对应值
     */
    public static boolean scanningRecord(int[] nums) {
        int len = nums.length;
        // 记录扫描过的最小值的下标
        int x = Integer.MAX_VALUE;
        // 记录扫描过的第二小值的下标
        int y = Integer.MAX_VALUE;
        // 开始执行扫描
        for (int i = 0; i < len; i++) {
            // 判断当前值是否小于最小值
            // 小于最小值则更新最小值
            if (nums[i] <= x) {
                // 替换最小值
                x = nums[i];
            } else if (y >= nums[i] && nums[i] > x) {
                // 判断是否在最小值和中间值之间，如果是则更新中间值
                y = nums[i];
            } else {
                // 大于前两个值，则是正确的数
                return true;
            }
        }
        // 最终没找到则返回false
        return false;
    }

