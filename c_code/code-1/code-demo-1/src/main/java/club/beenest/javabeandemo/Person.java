package club.beenest.javabeandemo;

/**
 * 人信息类
 *
 * @author cyuxuan
 */

public class Person {
    /**
     * 姓名
     */
    private String name;

    /**
     * 年龄
     */
    private String age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
